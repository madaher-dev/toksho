// calculate agerage rating lecture 168
reviewSchema.statics.calcAverageRatings = async function(resourceID) {
  const stats = await this.aggregate([
    {
      $match: { resource: resourceID }
    },
    {
      $group: {
        _id: '$resource',
        nRating: { $sum: 1 },
        avgRating: { $avg: '$rating' }
      }
    }
  ]);
  // console.log(stats);

  if (stats.length > 0) {
    await Resource.findByIdAndUpdate(resourceID, {
      ratingsQuantity: stats[0].nRating,
      ratingsAverage: stats[0].avgRating
    });
  } else {
    await Resource.findByIdAndUpdate(resourceID, {
      ratingsQuantity: 0,
      ratingsAverage: 4.5
    });
  }
};

